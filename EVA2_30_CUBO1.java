public class EVA2_30_CUBO1 {

    public static void main(String[] args) {
        int cubo[][][] = new int[3][4][5];
        
        for (int i = 0; i < cubo.length; i++) {
            for (int j = 0; j < cubo[i].length; j++) {
                for (int k = 0; k < 5; k++) {
                    System.out.println("[" + cubo[i][j][k] +"]");
                System.out.println("");    
                System.out.println("");
                    
                }
                
            }
            
        }
    }
}
